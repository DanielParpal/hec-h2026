// 1. Définition d'une variable (espace dans la mémoire)
var sousTotal;

// 2. Assignation d'une valeur à la variable
// Un truc est de penser à l'assignation de droite à gauche. Ici, on place 150 dans la variable sousTotal.
sousTotal = 150;

console.log(sousTotal);
console.log(sousTotal);
console.log(sousTotal);
console.log(sousTotal);

// On peut accéder à la variable avec son nom
console.log(sousTotal);

// On peut placer une nouvelle valeur après l'assignation initiale
sousTotal = sousTotal + 30; // 180
console.log(sousTotal);
console.log(sousTotal);
console.log(sousTotal);
console.log(sousTotal);

console.log(sousTotal + 20);
console.log(sousTotal);

